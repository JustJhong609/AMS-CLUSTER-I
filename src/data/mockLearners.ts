const calcBirthdate = (age: number, idx: number): string => {
  const year = new Date().getFullYear() - age;
  const month = String((idx % 12) + 1).padStart(2, '0');
  const day = String(((idx * 2) % 28) + 1).padStart(2, '0');
  return `${year}-${month}-${day}`;
};

